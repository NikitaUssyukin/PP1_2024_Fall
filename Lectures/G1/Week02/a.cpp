#include <iostream>

using namespace std;

int main() {

    int a = 2;
    int b = 3;

    // increment amnd decrement
    // ++ and --

    // preincrement 
    cout << ++a << endl;

    // postincrement
    cout << a++ << endl;
    cout << a << endl;

    // predecrement 
    cout << --b << endl;

    // postdecrement
    cout << b-- << endl;
    cout << b << endl;

    return 0;
}