#include <iostream>
#include <cmath>

using namespace std;

// math functions for rounding 
// round, ceil, floor

int main() {
    double a;

    cin >> a;

    cout << "round(a): " << round(a) << endl;
    cout << "ceil(a): " << ceil(a) << endl;
    cout << "floor(a): " << floor(a) << endl;

    return 0;
}